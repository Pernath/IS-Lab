package Modelo;

/**
 *
 * @author carlos
 */
public class Telefono {
    private int id;
    private String numero;

    public Telefono(){
	this.id = id;
	this.numero = numero;
    }
    public int getId() {
        return id;
    }

    public String getNumero() {
        return numero;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }
    
}


